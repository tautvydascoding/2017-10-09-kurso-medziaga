                            <p class="pagrindinis-tekstas">
                                Tik išvažiavus iš Juodkrantės Nidos link, dar nepakilus į statųjį Avino<br>
                                kalną, sengirėje yra kitas - Garnių kalnas, vardą gavęs nuo netoliese<br>
                                įsikūrusios pilkųjų garnių ir didžiųjų kormoranų kolonijos. Tai viena<br>
                                didžiausių šių paukščių kolonijų visoje Europoje. 2000 metų duomenimis,<br>
                                joje perėjo 582 pilkųjų garnių ir 1361 didžiųjų kormoranų pora. Vargu<br>
                                ar įmanoma atsakyti į klausimą, kada garniai apsigyveno Juodkrantės<br>
                                apylinkėse? XIX a. viduryje jie buvo įprasta kasdienybės detalė. Galima<br>
                                drąsiai tvirtinti, kad šie ilgakojai paukščiai gyveno XVII a. ar net<br>
                                gerokai seniau. Kas paskatino garnius perkelti savo lizdavietes iš<br>
                                šiaurinių Juodkrantės apylinkių į pietines? Istoriniai šaltiniai<br>
                                pasakoja apie jų kovą su agresyviu atėjūnu - kormoranais. Kormoranai<br>
                                (Phalacrocorax carbo L. arba Carbo cormoranus), kitaip vadinami jūrų<br>
                                krankliais ”Corvus marinus”, Juodkrantėje pasirodė 1803 m. Jie<br>
                                apsigyveno greta garnių ir ėmė užiminėti šių lizdavietes. Palaipsniui<br>
                                garniai buvo išstumti į pakraščius. Jie pradėjo migruoti piečiau<br>
                                Juodkrantės. Po 10 metų susidarė dvi atskiros garnių ir kormoranų<br>
                                kolonijos pietiniame ir šiauriniame Juodkrantės pakraštyje.
