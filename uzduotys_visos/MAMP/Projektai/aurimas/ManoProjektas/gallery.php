                                arba Jonines. Iš čia ir kilo Jono kalno vardas. Čia pasilinksminti iki
                                Pirmojo pasaulinio karo susirinkdavo muzikantai ir chorai iš Tilžės,
                                Rusnės bei Klaipėdos. Tik 1979 m. pradėjus ant Jono kalno statyti medines
                                skulptūras šis kalnas pradėtas vadinti Raganų skulptūrų ekspozicijos
                                kalnu, ar sutrumpintai Raganų kalnu. Raganų kalnas apipintas legendomis.
                                Sakoma, kad nuo neatmenamų laikų ne tik žmonės mėgo šį kalną, bet ir
                                velniai su raganomis čia savo puotas keldavę. Net pati deivė Ragė
                                apsilankydavo metiniuose trumpųjų naktų sąskrydžiuose.
                            </p>
