                                čia buvo užpustyti net keturi Naglių kaimai, kurie ir dabar dunkso giliai po
                                smėliu. Pasakojama, kad tuomet kopų slinkimo greitis buvo nuo 0,5 iki 15 metrų
                                per metus ir žmonės ilgai, tačiau nesėkmingai su jomis kovojo.
