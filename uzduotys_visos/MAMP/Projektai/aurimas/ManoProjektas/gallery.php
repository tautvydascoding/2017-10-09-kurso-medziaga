                            <p class="pagrindinis-tekstas">
                                Pirmojo Nidos švyturio statyba buvo pradėta po Vokietijos suvienijimo
                                karo XIX a. aštuntojo dešimtmečio pradžioje. 27 metrų aukščio raudonų
                                plytų šešiakampės formos bokštas buvo statomas kartu apželdinant
                                51,4 m aukščio Urbo kalną. Tam tikslui iki viršūnės buvo nutiestas
                                specialus lentinis kelias. Švyturys pradėjo veikti 1874 m. spalio 24 d.
                                Jo link vedė akmenimis grįstas takas su 200 laiptelių, kurie išlikę
                                iki šių dienų. Šiuos akmeninius laiptus klojo prancūzų kariai, patekę
                                į vokiečių nelaisvę 1870-1871 m. per Prancūzijos-Prūsijos karą.
                                Senajame Nidos švyturyje buvo įtaisytas Fresnelio optinės sistemos
                                šviesos aparatas, kurio degikliui buvo naudojama mineralinė alyva.
                                Kas dešimt sekundžių jis siuntė 4 sekundžių blyksnį matoma jūroje
                                21 jūrmylės atstumu. Pirmasis Nidos švyturys veikė iki Antrojo
                                pasaulinio karo pabaigos, kai 1944 m., besitraukiantys vokiečių
                                kareiviai jį susprogdino. Tačiau jau 1945 m. jis buvo atstatytas,
                                o 1953 m. – perstatytas
