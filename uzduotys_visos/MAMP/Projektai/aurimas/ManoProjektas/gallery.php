                            <h4 class="antrasciu-tekstas">kormoranų kolonija</h4>
                            <p class="pagrindinis-tekstas">
                                Tik išvažiavus iš Juodkrantės Nidos link, dar nepakilus į statųjį Avino
                                kalną, sengirėje yra kitas - Garnių kalnas, vardą gavęs nuo netoliese
                                įsikūrusios pilkųjų garnių ir didžiųjų kormoranų kolonijos. Tai viena
                                didžiausių šių paukščių kolonijų visoje Europoje. 2000 metų duomenimis,
                                joje perėjo 582 pilkųjų garnių ir 1361 didžiųjų kormoranų pora. Vargu
                                ar įmanoma atsakyti į klausimą, kada garniai apsigyveno Juodkrantės
                                apylinkėse? XIX a. viduryje jie buvo įprasta kasdienybės detalė. Galima
                                drąsiai tvirtinti, kad šie ilgakojai paukščiai gyveno XVII a. ar net
                                gerokai seniau. Kas paskatino garnius perkelti savo lizdavietes iš
                                šiaurinių Juodkrantės apylinkių į pietines? Istoriniai šaltiniai
                                pasakoja apie jų kovą su agresyviu atėjūnu - kormoranais. Kormoranai
                                (Phalacrocorax carbo L. arba Carbo cormoranus), kitaip vadinami jūrų
                                krankliais ”Corvus marinus”, Juodkrantėje pasirodė 1803 m. Jie
                                apsigyveno greta garnių ir ėmė užiminėti šių lizdavietes. Palaipsniui
                                garniai buvo išstumti į pakraščius. Jie pradėjo migruoti piečiau
                                Juodkrantės. Po 10 metų susidarė dvi atskiros garnių ir kormoranų
                                kolonijos pietiniame ir šiauriniame Juodkrantės pakraštyje.
                            </p>
