                                gyvenvietę. 1675–1854 metais čia buvo užpustyti keturi Naglių kaimai.
                                Apie kopą veda 1,8 km Parnidžio pažintinis takas. 2011 m. saulės laikrodis
                                atstatytas. Tai 13,8 m aukščio akmens stulpas, sveriantis 36 tonas. Ant
                                šalia jo iškilusių nedidelių pakopėlių, padengtų granito plokštėmis,
                                iškaltos valandų ir pusvalandžių įraižos, taip pat po vieną kiekvienam
                                mėnesiui, ir dar keturioms saulėgrįžoms ir lygiadieniams.
