                            <p class="pagrindinis-tekstas">
                                Gintaro įlanka – nedidelė Kuršių marių įlankėlė šiaurinėje
                                Juodkrantės dalyje. Jos vardas siejamas su gintaro gavybos
                                laikais praeityje. 1855 m. ties Juodkrante begilinant laivybai
                                Kuršių marių dugną, dumble aptikta gintaro. Šiuo atradimu
                                netrukus susidomėjo įvairūs verslininkai. Pirmasis organizuotai
                                kasti purvą iš marių dugno gintaro paieškoms ėmėsi buvęs
                                malūnininkas, laivų savininkas, vėliau Klaipėdos smuklininkas
                                Vilhelmas Stantynas. Pastarajam kompaniją palaikė pirkliai
                                iš Dancigo, buvo įkurta gintaro kasimo bendrovė „V. Stantien
                                and M. Becker“. Valdžia taip pat neliko nuošaly, nes gilino
                                laivų plaukiojimo vagą tarp Klaipėdos ir nerijos pakrantės.
                                Suvienijus jėgas, bendrovė ėmė turtėti. Gintaro kasyba paskatino
                                gyvenvietės augimą. Bendrovei reikėjo pigios darbo jėgos, todėl
                                ji skatino į gintaro kasimą įsitraukti ir vietos gyventojus.
                                Didėjant apimtims, į šiaurę nuo Juodkrantės buvo pastatyti
                                erdvūs barakai, laivų remonto, narų aprangos dirbtuvės,
                                įrengta uosto krantinė.
                            </p>
